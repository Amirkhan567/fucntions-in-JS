
// with return keyword //
function addition(a, b){
    return a+b
    console.log(a+b)
    console.log(a+b)
}

console.log(addition(3,5))
console.log(addition(3,8))

// without return keyword //

function subtraction(x, y){

    console.log(x-y)
    console.log(x-y)
}
subtraction(3,5)
subtraction(3,8)

